int sum(int x, int y) 
{	
return x + y;
}
int subtract(int x, int y) 
{	
return x - y;
}
int cheng(int x, int y) 
{	
return x * y;
}
int max(int x, int y) 
{	
  if(x>=y) return x;
  else     return y;
}
int min(int x, int y)
{
  if(x<=y) return x;
  else     return y;
}
